# Basic Python Programming Examples

# Variables
name = "John"
age = 30
print(f"Name: {name}, Age: {age}")

# Loops
print("Looping through numbers:")
for i in range(5):
    print(i)

# Functions
def greet(person):
    return f"Hello, {person}!"

print(greet(name))

# Data Structures
# List
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# Dictionary
person_info = {"name": "John", "age": 30}
print("Person Info:", person_info)